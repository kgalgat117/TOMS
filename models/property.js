var DB = require('./../config/db')
var mongoose = require('mongoose')

let propertySchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    owner: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'users',
        required: true
    },
    address1: {
        type: String,
        required: true
    },
    address2: {
        type: String,
        required: true
    },
    country: {
        type: String,
        required: true
    },
    state: {
        type: String,
        required: true
    },
    city: {
        type: String,
        required: true
    },
    pincode: {
        type: Number,
        required: true
    },
    created_on: {
        type: Date,
        default: Date.now()
    }
})

let propertyModel = DB.model('properties', propertySchema)

module.exports = propertyModel